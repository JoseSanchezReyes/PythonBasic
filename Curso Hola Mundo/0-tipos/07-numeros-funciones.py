import math

print(round(1.3))       # 1 -> redondea un valor
print(round(1.6))       # 2
print(round(1.5))       # 2
print(abs(-77))         # 77 -> Entrega el valor absoluto
print(abs(55))          # 55


# Importar un modulo para trabajar con numeros -> import math
print(math.ceil(1.1))          # 2 -> Retorna el valor mas cercano hacia arriba
print(math.floor(1.9999))      # 1 -> Retorna el valor mascercano hacia abajo
print(math.isnan(23))          # False -> por que es un numero
# print(math.isnan("93"))      # Error -> solo funciona para numero reales
print(math.pow(10, 3))         # 1000 -> Potencia
print(math.sqrt(9))            # 3 -> Raiz cuadrada

